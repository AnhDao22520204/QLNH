    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Lấy email, mã xác minh và mật khẩu mới từ người dùng
        String email = txtEmail.getText();
        String verifyCode = txtVerifyCode.getText();
        String newPassword = txtNewPassword.getText();

        // Tạo đối tượng ServiceUser
        ServiceUser serviceUser = new ServiceUser();

        try {
            // Lấy userID từ email
            int userID = serviceUser.getUserIdByEmail(email);

            // Kiểm tra mã xác minh
            if (serviceUser.verifyCodeWithUser(userID, verifyCode)) {
                // Nếu mã xác minh đúng, thay đổi mật khẩu
                serviceUser.changePassword(userID, newPassword);
                JOptionPane.showMessageDialog(null, "Mật khẩu đã được thay đổi thành công.");
            } else {
                // Nếu mã xác minh sai, thông báo lỗi
                JOptionPane.showMessageDialog(null, "Mã xác minh không đúng.");
            }
        } catch (SQLException ex) {
            // Xử lý ngoại lệ nếu có lỗi xảy ra
            JOptionPane.showMessageDialog(null, "Đã xảy ra lỗi: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

