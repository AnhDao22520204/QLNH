    private void btnSentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSentActionPerformed
        // TODO add your handling code here:
        String recipientEmail = txtEmail.getText();
    
        // Tạo đối tượng ServiceUser và ServiceMail
        ServiceUser serviceUser = new ServiceUser();
        ServiceMail serviceMail = new ServiceMail();
        
        try {
        // Kiểm tra xem email có tồn tại trong hệ thống hay không
        if (serviceUser.checkEmailExists(recipientEmail)) {
            // Nếu email tồn tại, gửi mã xác minh qua email
            int randomCode = serviceMail.sendResetCode(recipientEmail);
            
            // Lưu mã xác minh vào cơ sở dữ liệu
            serviceUser.saveVerifyCode(recipientEmail, randomCode);
            
            // Thông báo cho người dùng rằng mã xác minh đã được gửi
            JOptionPane.showMessageDialog(null, "Mã xác minh đã được gửi đến email của bạn.");
        } else {
            // Nếu email không tồn tại, thông báo cho người dùng biết
            JOptionPane.showMessageDialog(null, "Email không tồn tại trong hệ thống.");
        }
        } catch (SQLException ex) {
            // Xử lý ngoại lệ nếu có lỗi xảy ra trong quá trình kiểm tra email hoặc gửi mã xác minh
            JOptionPane.showMessageDialog(null, "Đã xảy ra lỗi: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSentActionPerformed

