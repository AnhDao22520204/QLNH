    private void tbtableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbtableMouseClicked
        int selectedRow = tbtable.getSelectedRow();

        // Kiểm tra xem người dùng đã chọn dòng nào chưa
        if (selectedRow != -1) {
            // Lấy dữ liệu từ bảng và hiển thị lên các ô nhập liệu
            txtmamonan.setText(tbtable.getValueAt(selectedRow, 0).toString());
            txtsoluong.setText(tbtable.getValueAt(selectedRow, 1).toString());
            txttenmonan.setText(tbtable.getValueAt(selectedRow, 2).toString());
            txtdvt.setText(tbtable.getValueAt(selectedRow, 3).toString());
            txtsoluong.setText(tbtable.getValueAt(selectedRow, 4).toString());
            txtgia.setText(tbtable.getValueAt(selectedRow, 5).toString());
        }
    }//GEN-LAST:event_tbtableMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        MONAN monan = new MONAN();
        try {          
            // Kiểm tra mã món ăn đã tồn tại hay không
            int MAMONAN = Integer.parseInt(txtmamonan.getText());
            int SOLUONG = Integer.parseInt(txtsoluong.getText());
            int GIA = Integer.parseInt(txtgia.getText());
            
            if (MONANDAO.isMAMONANTonTai(MAMONAN)) {
                JOptionPane.showMessageDialog(null, "Mã món ăn đã tồn tại. Vui lòng nhập một mã món ăn khác.");
                return; // Dừng việc thêm mới nếu mã đã tồn tại
            }

            // Kiểm tra và thêm món ăn mới
            if (txtmamonan.getText().isEmpty() || txtsoluong.getText().isEmpty() || txttenmonan.getText().isEmpty() || txtdvt.getText().isEmpty() || txtloai.getText().isEmpty() || txtgia.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ thông tin.");
            } else {
                monan.setMAMONAN(MAMONAN); 
                monan.setSOLUONG(SOLUONG);
                monan.setTENMONAN(txttenmonan.getText());
                monan.setDVT(txtdvt.getText());                                            
                monan.setLOAI(txtloai.getText());
                monan.setGIA(GIA);
                MONANDAO.save(monan);

                DefaultTableModel dtm = (DefaultTableModel) tbtable.getModel();
                dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
                ArrayList<MONAN> list = MONANDAO.getAllRecords();
                for (MONAN monanObj : list) {
                    dtm.addRow(new Object[]{monanObj.getMAMONAN(), monanObj.getSOLUONG(), monanObj.getTENMONAN(), monanObj.getDVT(), monanObj.getLOAI(), monanObj.getGIA()});
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đúng định dạng số cho Mã món ăn.");
        }
    }//GEN-LAST:event_btnthemActionPerformed

