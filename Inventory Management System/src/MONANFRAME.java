    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        int MAMONAN = Integer.parseInt(txtmamonan.getText());
        int SOLUONG = Integer.parseInt(txtsoluong.getText());
        String TENMONAN = txttenmonan.getText();
        String DVT = txtdvt.getText();
        String LOAI = txtloai.getText();
        int GIA = Integer.parseInt(txtgia.getText());

// Tạo một đối tượng món ăn mới với thông tin vừa lấy được
        MONAN monan = new MONAN();
        monan.setMAMONAN(MAMONAN);
        monan.setSOLUONG(SOLUONG);
        monan.setTENMONAN(TENMONAN);
        monan.setDVT(DVT);
        monan.setLOAI(LOAI);
        monan.setGIA(GIA);

// Gọi phương thức update từ lớp MONANDAO để cập nhật dữ liệu
        MONANDAO.update(monan);

// Cập nhật lại bảng hiển thị dữ liệu
        DefaultTableModel dtm = (DefaultTableModel) tbtable.getModel();
        dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
        ArrayList<MONAN> list = MONANDAO.getAllRecords();
        for (MONAN monanObj : list) {
            dtm.addRow(new Object[]{monanObj.getMAMONAN(), monanObj.getSOLUONG(), monanObj.getTENMONAN(), monanObj.getDVT(), monanObj.getLOAI(), monanObj.getGIA()});
        }
    }//GEN-LAST:event_btnsuaActionPerformed

