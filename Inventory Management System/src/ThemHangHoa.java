    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        HangHoa hanghoa = new HangHoa();
        hanghoa.setMaHH(Integer.parseInt(txtMaHH.getText()));
        hanghoa.setTenHH(txtTenHH.getText());
        hanghoa.setSoLuong(Integer.parseInt(txtSoLuong.getText()));
        hanghoa.setGia(Integer.parseInt(txtGia.getText()));
        hanghoa.setNhaCungCap(txtMaHH.getText());
        Date HSD = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsedHSD = sdf.parse(txtHSD.getText());
            HSD = new Date(parsedHSD.getTime());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sai định dạng ngày tháng năm vào làm. Vui lòng nhập lại theo định dạng yyyy-MM-dd.");
            return;
        }
        hanghoa.setHSD(HSD);
   
        
     

        HangHoaDAO.save(hanghoa);

         DefaultTableModel dtm = (DefaultTableModel) tbHangHoa.getModel();
    dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
    ArrayList<HangHoa> list = HangHoaDAO.getAllRecords();
    for (HangHoa hanghoaObj : list) {
        dtm.addRow(new Object[]{hanghoaObj.getMaHH(), hanghoaObj.getTenHH(), hanghoaObj.getSoLuong(), hanghoaObj.getGia(), hanghoaObj.getNhaCungCap(), hanghoaObj.getHSD()});
    }


    }//GEN-LAST:event_btnXacNhanActionPerformed

