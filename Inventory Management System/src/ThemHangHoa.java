    private void tbHangHoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHangHoaMouseClicked
        // TODO add your handling code here:
        int selectedRow = tbHangHoa.getSelectedRow();

        // Kiểm tra xem người dùng đã chọn dòng nào chưa
        if (selectedRow != -1) {
            // Lấy dữ liệu từ bảng và hiển thị lên các ô nhập liệu
            txtMaHH.setText(tbHangHoa.getValueAt(selectedRow, 0).toString());
            txtTenHH.setText(tbHangHoa.getValueAt(selectedRow, 1).toString());
            txtSoLuong.setText(tbHangHoa.getValueAt(selectedRow, 2).toString());
            txtGia.setText(tbHangHoa.getValueAt(selectedRow, 3).toString());
            txtNhaCungCap.setText(tbHangHoa.getValueAt(selectedRow, 4).toString());
            txtHSD.setText(tbHangHoa.getValueAt(selectedRow, 5).toString());
        }
                

    }//GEN-LAST:event_tbHangHoaMouseClicked

    private void txtSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSuaActionPerformed
   try {
        int maHH = Integer.parseInt(txtMaHH.getText());
        String tenHH = txtTenHH.getText();
        int soLuong = Integer.parseInt(txtSoLuong.getText());
        double gia = Double.parseDouble(txtGia.getText());
        String nhaCungCap = txtNhaCungCap.getText();
        Date HSD = Date.valueOf(txtHSD.getText()); // Chú ý: Kiểm tra xem định dạng ngày tháng có đúng không
        
        // Tạo đối tượng hàng hóa với thông tin đã chỉnh sửa
        HangHoa editedHangHoa = new HangHoa();
        editedHangHoa.setMaHH(maHH);
        editedHangHoa.setTenHH(tenHH);
        editedHangHoa.setSoLuong(soLuong);
        editedHangHoa.setGia((int) gia);
        editedHangHoa.setNhaCungCap(nhaCungCap);
        editedHangHoa.setHSD(HSD);

        // Cập nhật thông tin hàng hóa trong cơ sở dữ liệu
        HangHoaDAO.update(editedHangHoa); // Đảm bảo rằng bạn đã khai báo và khởi tạo lớp HangHoaDAO ở một nơi khác trong mã của bạn

        // Cập nhật lại bảng hiển thị
        DefaultTableModel dtm = (DefaultTableModel) tbHangHoa.getModel();
        dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
        ArrayList<HangHoa> list = HangHoaDAO.getAllRecords();
        for (HangHoa hanghoaObj : list) {
            dtm.addRow(new Object[]{hanghoaObj.getMaHH(), hanghoaObj.getTenHH(), hanghoaObj.getSoLuong(), hanghoaObj.getGia(), hanghoaObj.getNhaCungCap(), hanghoaObj.getHSD()});
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Dữ liệu không hợp lệ! Vui lòng nhập lại.");
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(null, "Định dạng ngày tháng không hợp lệ! Vui lòng nhập theo định dạng YYYY-MM-DD.");
    }
    }//GEN-LAST:event_txtSuaActionPerformed

