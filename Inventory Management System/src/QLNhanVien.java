    private void BtnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSuaActionPerformed
        int maNV = Integer.parseInt(txtMaNV.getText());
        String hoTen = txtHoTen.getText();
        String diaChi = txtDiaChi.getText();
        String sdt = txtSDT.getText();

// Kiểm tra định dạng và chuyển đổi ngày vào làm từ chuỗi thành đối tượng Date
        Date ngVaoLam = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsedNgVaoLam = sdf.parse(txtNgVaoLam.getText());
            ngVaoLam = new Date(parsedNgVaoLam.getTime());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sai định dạng ngày tháng năm vào làm. Vui lòng nhập lại theo định dạng yyyy-MM-dd.");
            return;
        }

// Kiểm tra định dạng và chuyển đổi ngày sinh từ chuỗi thành đối tượng Date
        Date ngSinh = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date parsedNgSinh = sdf.parse(txtNgSinh.getText());
            ngSinh = new Date(parsedNgSinh.getTime());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sai định dạng ngày tháng năm sinh. Vui lòng nhập lại theo định dạng yyyy-MM-dd.");
            return;
        }

// Kiểm tra ngày vào làm không được lớn hơn ngày hiện tại
        java.util.Date currentDate = new java.util.Date();
        if (ngVaoLam.after(currentDate)) {
            JOptionPane.showMessageDialog(null, "Ngày vào làm không được lớn hơn ngày hiện tại.");
            return;
        }

// Tạo một đối tượng NhanVien mới với thông tin vừa lấy được
        NhanVien nhanvien = new NhanVien();
        nhanvien.setMaNV(maNV);
        nhanvien.setHoTen(hoTen);
        nhanvien.setDiaChi(diaChi);
        nhanvien.setNgVaoLam(ngVaoLam);
        nhanvien.setNgSinh(ngSinh);
        nhanvien.setSDT(sdt);

// Gọi phương thức update từ lớp NhanVienDAO để cập nhật dữ liệu
        NhanVienDAO.update(nhanvien);

// Cập nhật lại bảng hiển thị dữ liệu
        DefaultTableModel dtm = (DefaultTableModel) tbNhanVien.getModel();
        dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
        ArrayList<NhanVien> list = NhanVienDAO.getAllRecords();
        for (NhanVien nhanvienObj : list) {
            dtm.addRow(new Object[]{nhanvienObj.getMaNV(), nhanvienObj.getHoTen(), nhanvienObj.getDiaChi(), nhanvienObj.getNgSinh(), nhanvienObj.getNgVaoLam(), nhanvienObj.getSDT()});
        }

    }//GEN-LAST:event_BtnSuaActionPerformed

