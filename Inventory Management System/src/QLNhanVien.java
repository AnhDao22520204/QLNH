    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        NhanVien nhanvien = new NhanVien();
        try {
            // Lấy ngày hiện tại
            java.util.Date currentDate = new java.util.Date();

            // Chuyển đổi ngày hiện tại thành định dạng phù hợp
            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String formattedCurrentDate = dateFormat.format(currentDate);

            // Kiểm tra mã nhân viên đã tồn tại hay không
            int maNV = Integer.parseInt(txtMaNV.getText());
            if (NhanVienDAO.isMaNVTonTai(maNV)) {
                JOptionPane.showMessageDialog(null, "Mã nhân viên đã tồn tại. Vui lòng nhập một mã nhân viên khác.");
                return; // Dừng việc thêm mới nếu mã đã tồn tại
            }

            // Kiểm tra và thêm nhân viên mới
            if (txtMaNV.getText().isEmpty() || txtHoTen.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtNgSinh.getText().isEmpty() || txtNgVaoLam.getText().isEmpty() || txtSDT.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Vui lòng nhập đủ thông tin.");
            } else {
                nhanvien.setMaNV(maNV);
                nhanvien.setHoTen(txtHoTen.getText());
                nhanvien.setDiaChi(txtDiaChi.getText());

                // Chuyển đổi ngày vào làm từ chuỗi thành đối tượng Date
                java.util.Date parsedNgVaoLam = null;
                try {
                    parsedNgVaoLam = dateFormat.parse(txtNgVaoLam.getText());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Sai định dạng ngày tháng năm vào làm. Vui lòng nhập lại theo định dạng yyyy-MM-dd.");
                    return;
                }

                // So sánh ngày vào làm với ngày hiện tại
                if (parsedNgVaoLam.after(currentDate)) {
                    JOptionPane.showMessageDialog(null, "Ngày vào làm không được lớn hơn ngày hiện tại.");
                    return;
                }

                // Convert thành java.sql.Date
                Date ngVaoLam = new Date(parsedNgVaoLam.getTime());

                // Chuyển đổi ngày sinh từ chuỗi thành đối tượng Date
                java.util.Date parsedNgSinh = null;
                try {
                    parsedNgSinh = dateFormat.parse(txtNgSinh.getText());
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Sai định dạng ngày tháng năm sinh. Vui lòng nhập lại theo định dạng yyyy-MM-dd.");
                    return;
                }

                // Convert thành java.sql.Date
                Date ngSinh = new Date(parsedNgSinh.getTime());

                nhanvien.setNgSinh(ngSinh);
                nhanvien.setNgVaoLam(ngVaoLam);
                nhanvien.setSDT(txtSDT.getText());

                NhanVienDAO.save(nhanvien);

                DefaultTableModel dtm = (DefaultTableModel) tbNhanVien.getModel();
                dtm.setRowCount(0); // Xóa hết các dòng hiện có trong bảng
                ArrayList<NhanVien> list = NhanVienDAO.getAllRecords();
                for (NhanVien nhanvienObj : list) {
                    dtm.addRow(new Object[]{nhanvienObj.getMaNV(), nhanvienObj.getHoTen(), nhanvienObj.getDiaChi(), nhanvienObj.getNgSinh(), nhanvienObj.getNgVaoLam(), nhanvienObj.getSDT()});
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đúng định dạng số cho Mã Nhân Viên.");
        }

    }//GEN-LAST:event_btnThemActionPerformed

